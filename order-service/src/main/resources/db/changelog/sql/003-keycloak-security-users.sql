SET user_role = CASE user_role
    WHEN 'CLIENT' THEN 'USER'
    WHEN 'STORAGE_ADMIN' THEN 'WAREHOUSE_ADMIN'
    WHEN 'SYSTEM_ADMIN' THEN 'ADMIN'
    ELSE user_role
END;

